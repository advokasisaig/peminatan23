function cekHasil() {
  const namaInput = document.getElementById("nama").value.trim().toLowerCase();
  const nimInput = document.getElementById("nim").value.trim();
  const hasilDiv = document.getElementById("hasil");
  const loadingDiv = document.getElementById("loading");

  const audioSuspense = document.getElementById("audioSuspense");
  const audioSelamat = document.getElementById("audioSelamat");

  // Reset tampilan sebelumnya
  hasilDiv.classList.add("hidden");
  hasilDiv.innerHTML = "";
  loadingDiv.classList.remove("hidden");

  // Mulai efek suspense
  audioSuspense.currentTime = 0;
  audioSuspense.play();

  setTimeout(() => {
    const hasil = dataPeminatan.find(item =>
      item.nama.toLowerCase() === namaInput && item.nim === nimInput
    );

    // Stop suspense
    audioSuspense.pause();
    audioSuspense.currentTime = 0;

    loadingDiv.classList.add("hidden");

    if (hasil) {
      // Mainkan suara keberhasilan
      audioSelamat.currentTime = 0;
      audioSelamat.play();

      hasilDiv.innerHTML = `
        <h2>🎉 Selamat, ${hasil.nama}!</h2>
        <p>NIM: ${hasil.nim}</p>
        <p>Anda diterima di peminatan:</p>
        <h3>${hasil.peminatan}</h3>
      `;
    } else {
      hasilDiv.innerHTML = `
        <h2>Data tidak ditemukan.</h2>
        <p>Pastikan nama dan NIM diinput dengan benar.</p>
      `;
    }

    hasilDiv.classList.remove("hidden");
  }, 3500); // Durasi suspense
}
